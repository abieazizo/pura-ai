#!/usr/bin/env node
/**
 * Bundle Vercel serverless function source into a self-contained JS file.
 *
 * Why this exists: Vercel's built-in TypeScript compilation for files in
 * `api/` compiles each `.ts` to `.js` BUT keeps bare-specifier imports
 * (`from '../../server/lib/handlers'`) instead of inlining them. Those
 * bare specifiers cannot be resolved at runtime under Node's ESM loader
 * (`ERR_MODULE_NOT_FOUND`), so the function fails on first invocation
 * with `FUNCTION_INVOCATION_FAILED`.
 *
 * Solution: pre-bundle the function with esbuild before deploy. esbuild
 * walks the import graph and inlines every transitive dependency
 * (including `server/lib/handlers`, `server/openai/openai-client`,
 * `src/ai/validation`, etc.) into ONE self-contained `.js` file, which
 * Vercel then deploys verbatim. No bare specifiers, no runtime
 * resolution failures.
 *
 * Source-of-truth file: `api/proxy/_handler.ts` (underscore prefix makes
 * Vercel ignore it — only the bundled output is deployed).
 *
 * Bundled output: `api/proxy/[method].js` — what Vercel actually serves.
 *
 * The output file is committed (regenerated by `npm run build:functions`
 * or by the Vercel `buildCommand` chain).
 */

import { build } from 'esbuild';
import { existsSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const projectRoot = resolve(__dirname, '..');

const entryPoint = resolve(projectRoot, 'api/proxy/_handler.ts');
const outFile = resolve(projectRoot, 'api/proxy/[method].js');

if (!existsSync(entryPoint)) {
  console.error(`[bundle-functions] entry not found: ${entryPoint}`);
  process.exit(1);
}

await build({
  entryPoints: [entryPoint],
  outfile: outFile,
  bundle: true,
  platform: 'node',
  target: 'node20',
  // CommonJS keeps things simple: no need for "type":"module" anywhere,
  // no `.js` extension requirements on internal imports, no Node ESM
  // resolution quirks. The bundle is self-contained.
  format: 'cjs',
  // Pack OpenAI SDK and other deps into the bundle so /var/task doesn't
  // need to resolve them at runtime via node_modules.
  packages: 'bundle',
  // Vercel's Node runtime provides these as built-ins — leave them external.
  external: [],
  // Keep stack traces readable.
  sourcemap: false,
  // Minify lightly — keeps bundle small but stack traces still useful.
  minify: false,
  // Tree-shake unused code from imports.
  treeShaking: true,
  logLevel: 'info',
});

console.log(`[bundle-functions] wrote ${outFile}`);
